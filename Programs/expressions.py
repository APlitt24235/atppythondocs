# Practicing Expressions

# 1: This is how you use assignment operators:
x = 4
# 2: You can add/subtract/multiply/divide with an assignment operator, like this:
x += 2
x -= 2
x *= 2
x /= 2
# 3: You can compare two equations with a comparison operator, like this:
y = x > 2
# 4: You can add/sub/mul/div numbers in an expression, like this:
x = 4 + 6 - 8 * 10 / 12
